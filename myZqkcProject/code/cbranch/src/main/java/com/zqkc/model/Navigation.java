package com.zqkc.model;

import org.springframework.stereotype.Component;

/**
 * @author Administrator Mode层 导航 封装对象
 *
 */
@Component("navigation")
public class Navigation {
	
	/**
	 * 导航编号
	 */
	private int id;
	/**
	 * 导航名
	 */
	private String name;
	/**
	 * 标题
	 */
	private String title;
	/**
	 * 预留字段
	 */
	private String remark;
	/**
	 * 权重
	 */
	private int power;
	/**
	 * 连接地址
	 */
	private String url;
	/**
	 * 父编号
	 */
	private int pid;
	/**
	 * 层级
	 */
	private int level;
	/**
	 * 是否删除
	 */
	private int isdel=1;
	
	public Navigation(){
		
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public int getPower() {
		return power;
	}

	public void setPower(int power) {
		this.power = power;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public int getPid() {
		return pid;
	}

	public void setPid(int pid) {
		this.pid = pid;
	}

	public int getLevel() {
		return level;
	}

	public void setLevel(int level) {
		this.level = level;
	}

	public int getIsdel() {
		return isdel;
	}

	public void setIsdel(int isdel) {
		this.isdel = isdel;
	}
	
	@Override
	public String toString() {
		// TODO Auto-generated method stub
		return "Navigation:{id="+id+";name="+name+";title="
				+title+";remark="+remark+";power="
		+power+";url="+url+";pid="+pid+"}";
	}
	
	
	
}
