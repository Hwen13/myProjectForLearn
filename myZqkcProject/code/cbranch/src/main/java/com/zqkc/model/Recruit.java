package com.zqkc.model;
/**
 * 
 * @author saddy
 * 招募表
 */
public class Recruit {
	
	 /**
	  * 招募编号
	  */
     private int id;
     /**
      * 职位名称
      */
     private String job;
     /**
      * 招聘要求标题
      */
     private String title1;
    /**招聘要求
     * 
     */
     private String requirement;
    /**
     * 任职资格标题
     */
     private String title2;
     /**
      * 任职资格
      */
     private String qualification;
    /**
     * 招聘人数
     */
     private int sum;
     /**
      * 权重
      */
     private int power;
     /**
      * 是否删除
      */
     private int isdel;
     /**
      * 无参构造
      */
	public Recruit() {
		
	}
	  /**
     * 有参构造
     */
	public Recruit(int id, String job, String title1, String requirement,
			String title2, String qualification, int sum, int power, int isdel) {
		super();
		this.id = id;
		this.job = job;
		this.title1 = title1;
		this.requirement = requirement;
		this.title2 = title2;
		this.qualification = qualification;
		this.sum = sum;
		this.power = power;
		this.isdel = isdel;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getJob() {
		return job;
	}
	public void setJob(String job) {
		this.job = job;
	}
	public String getTitle1() {
		return title1;
	}
	public void setTitle1(String title1) {
		this.title1 = title1;
	}
	public String getRequirement() {
		return requirement;
	}
	public void setRequirement(String requirement) {
		this.requirement = requirement;
	}
	public String getTitle2() {
		return title2;
	}
	public void setTitle2(String title2) {
		this.title2 = title2;
	}
	public String getQualification() {
		return qualification;
	}
	public void setQualification(String qualification) {
		this.qualification = qualification;
	}
	public int getSum() {
		return sum;
	}
	public void setSum(int sum) {
		this.sum = sum;
	}
	public int getPower() {
		return power;
	}
	public void setPower(int power) {
		this.power = power;
	}
	public int getIsdel() {
		return isdel;
	}
	public void setIsdel(int isdel) {
		this.isdel = isdel;
	}
	
	@Override
	public String toString() {
		return "Recruit [id=" + id + ", job=" + job + ", title1=" + title1
				+ ", requirement=" + requirement + ", title2=" + title2
				+ ", qualification=" + qualification + ", sum=" + sum
				+ ", power=" + power + ", isdel=" + isdel + "]";
	} 
    
     
}
